                        Генеральний прокурор Юрій Луценко заявив про спецконфіскацію до державного бюджету 1,5 млрд
                        гривень оточення колишнього президента Віктора Януковича. Про це повідомляє РБК-Україна,
                        посилаючись на Facebook генпрокурора Юрія Луценка.

                        "ГПУ відкрито шлях до виконання вироку суду в частині спеціальної конфіскації грошових коштів
                        злочинної організації часів Януковича в сумі 1 млрд 478 млн грн, які ближчими днями надійдуть на
                        рахунки Держказначейства й спрямовані на підтримку армії", - написав Луценко.

                        Луценко: конфісковані 1,5 млрд гривень оточення Януковича скоро надійдуть до бюджетуНагадаємо,
                        27 грудня Національне антикорупційне бюро закрило кримінальне провадження за фактом можливого
                        пособництва голови Державної служби фінансового моніторингу України Ігоря Черкаського в розтраті
                        "грошей Януковича" при їх спецконфіскації.

                        РБК-Україна писало, що Краматорський суд Донецької області 28 березня 2017 року ухвалив рішення
                        про конфіскацію у 10 компаній нерезидентів близько 1,5 млрд доларів. Як повідомив генпрокурор
                        Юрій Луценко, ці гроші належали екс-президенту Віктору Януковичу та його оточенню. В той же день
                        ці кошти були перераховані з рахунків цих компаній в Ощадбанку на рахунки Державного
                        казначейства.
