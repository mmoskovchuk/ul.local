                    Луценко.

                    На той момент Юрій Луценко вже був досить впливовою людиною в Соціалістичній партії. Віддаючи
                    належне лідеру СПУ Олександру Морозу, він все ж зазначає, що лідер партії сам знищив своє політичне
                    майбутнє. «На той момент він був великим, тому що створив єдину на пострадянському просторі
